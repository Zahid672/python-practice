"""We can specify a range of indexes by specifying 
where to start and where to end the range. When specifying a range, the return value will be a new list with the specified items."""

mylist = ["apple", "banana", "orange", "pomegranate", "dryfruits"]

print(mylist[1:3])

print(mylist[:2])

print(mylist[:])

print(mylist[2:])
